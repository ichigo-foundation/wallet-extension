var ICHIGOaddress = '0xca79E8c9f9D173C26bE2F7368299126B69a8195C'
var infuraWSS = 'wss://rinkeby.infura.io/ws/v3/e23bd3273a914d94bdb9e669c84a7563'
var web3 = new Web3(infuraWSS)

export default class Wallet {

    constructor(){
        this.contract = new web3.eth.Contract(ABI, ICHIGOaddress)
    }

    init(hdkey){
        this._privateKey = hdkey._privateKey
        this._publicKey = ethUtil.privateToPublic(this._privateKey)
        this._address = ethUtil.publicToAddress(this._publicKey)
    }

    async getBalance() {
        let amount =  await this.contract.methods.balanceOf(this.address()).call()
        return web3.utils.fromWei(amount)
    }

    async transfer(to, amount){
        amount = web3.utils.toWei(amount)
        let transaction = {
            from: this.address(),
            to: this.contract._address,
            gas: 200000,
            data: this.contract.methods.transfer(to, amount).encodeABI()
        }
        let signedTransation =  await web3.eth.accounts.signTransaction(transaction, this.privateKey())
        let receipt = await web3.eth.sendSignedTransaction(signedTransation.rawTransaction)
        return receipt
    }

    address( encoding = 'hex'){
        return '0x'+ this._address.toString(encoding)
    }

    publicKey( encoding = 'hex'){
        return this._publicKey.toString(encoding)
    }

    privateKey( encoding = 'hex'){
        return this._privateKey.toString(encoding)
    }

    getContract() {
        return ICHIGOaddress
    }

    setContract(addr) {
        ICHIGOaddress = addr
        this.contract = new web3.eth.Contract(ABI, ICHIGOaddress)
    }
}

